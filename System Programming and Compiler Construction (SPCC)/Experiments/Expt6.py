# Read the input grammar from a file
with open("Expt6.txt", "r") as file:
    grammar = file.read()

print("Recursive Descent Parsing For the following grammar: ")
print(grammar)

# Take the input string from the user
s = input("\nEnter the string to be checked: ")

i = 0

# Define the match function
def match(a):
    global s
    global i
    if i >= len(s):
        return False
    elif s[i] == a:
        i += 1
        return True
    else:
        return False

# Define the F function
def F():
    if match("("):
        if E():
            if match(")"):
                return True
            else:
                return False
        else:
            return False
    elif match("j"):
        return True
    else:
        return False

# Define the Tx function
def Tx():
    if match("*"):
        if F():
            if Tx():
                return True
            else:
                return False
        else:
            return False
    else:
        return True

# Define the T function
def T():
    if F():
        if Tx():
            return True
        else:
            return False
    else:
        return False

# Define the Ex function
def Ex():
    if match("+"):
        if T():
            if Ex():
                return True
            else:
                return False
        else:
            return False
    else:
        return True

# Define the E function
def E():
    if T():
        if Ex():
            return True
        else:
            return False
    else:
        return False

# Check whether the input string is generated by the grammar or not
if E():
    if i == len(s):
        print("String is accepted")
    else:
        print("String is not accepted")
else:
    print("String is not accepted")